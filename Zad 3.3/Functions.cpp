#include "Functions.h"

double multiply(double n1, double n2)
{
	int result = 0;
	result = n1 * n2;
	return result;
}


double multiply(double n1, double n2, double n3)
{
	int result = 0;
	result = n1 * n2 * n3;
		return result;
}


double multiply(double n1, double n2, double n3, double n4)
{
	int result = 0;
	result = n1 * n2 * n3 * n4;
	return result;
}